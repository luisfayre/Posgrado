                        <h2 id="inscripcion">Proceso de Inscripción</h2>

                        <h3>Sólo para primer semestre</h3>

                        <p>Después de cumplir con los pasos del proceso de admisión se procede a la inscripción en
                            el primer semestre:
                        </p>
                        <ol>
                            <li>El aspirante se presenta con coordinador de la maestría con los siguientes
                                documentos para
                                su revisión:
                            </li>
                            <ul>
                                <li class="listado">Dos fotografías tamaño infantil.</li>
                                <li class="listado">Registro impreso de que ya se dio Usted de alta en el CVU (Curriculum Vitae
                                    Único) en la página de
                                    <a href="http://siicyt.main.conacyt.mx:9096/psp/REGCYT3/?cmd=login&languageCd=ESP">
                                        CONACYT
                                    </a>.
                                </li>
                                <li class="listado">Curriculum vitae impreso.</li>
                                <li class="listado">Copia de credencial de elector.</li>
                                <li class="listado">Impresión de 
                                    <a href="https://consultas.curp.gob.mx/CurpSP/gobmx/inicio.jsp"> 
                                        CURP
                                    </a>.
                                </li>
                                <li class="listado">Acta de nacimiento.</li>
                                <li class="listado">Copia del formato FM9 (sólo en caso de ser extranjero).</li>
                                <li class="listado">Certificado médico emitido por una Institución del Sector Salud.</li>
                                <li class="listado">Copia de título, cédula o acta de examen profesional (validado por la Dirección
                                    General
                                    de Profesiones).
                                </li>
                                <li class="listado">Copia del Certificado de Estudios de la Licenciatura.</li>
                            </ul>
                            <li>El alumno lleva a Servicios Escolares los documentos correspondientes a ese
                                departamento
                                (Dos fotografías, certificado médico, copia de estudios de licenciatura y copia del
                                título,
                                cédula o acta de examen profesional).</li>
                            <li>El departamento de Servicios Escolares entrega constancia donde se notifica que el
                                alumno
                                entregó los documentos.</li>
                            <li>El coordinador de la maestría genera oficio donde se especifica las materias que
                                debe cursar
                                y el costo de cada materia incluyendo el costo de inscripción (FPI-01A).</li>
                            <li>El aspirante realiza el pago correspondiente en el Departamento de Recursos
                                Financieros.</li>
                            <li>El Departamento de Recursos Financieros elabora el recibo de pago.</li>
                            <li>Presentarse en tiempo y forma con el coordinador de la maestría para realizar la
                                inscripción
                                con la constancia firmada por el Departamento de Servicios Escolares (FSI-01).</li>
                            <li>El Coordinador inscribe al alumno e imprime horario.</li>
                            <li>El alumno firma el horario.</li>
                        </ol>
