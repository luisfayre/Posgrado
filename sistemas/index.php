                                    Admisión</span></p>
                            <li>Llenar la solicitud de ingreso (FSI-01). En este formato se solicitan datos
                                generales del
                                alumno aspirante, escuela de procedencia y documentación entregada. </li>
                            <li>Entrega la Carta de Exposición de Motivos. Es un documento impreso donde se exponen
                                los motivos
                                por los cuales desea ingresar a la maestría (una cuartilla mínimo). Este documento
                                debe
                                ser firmado por el aspirante.</li>
                            <li>Presentar dos cartas de recomendación de maestros de licenciatura (excluir maestros
                                del Consejo
                                de Posgrado del Tecnológico de Chihuahua II).</li>
                            <li>Presentarse a la entrevista con el Comité de Admisión, según la fecha establecida
                                por el
                                coordinador de la maestría en conjunto con los miembros del Comité de Admisión.</li>
                            <li>Presentarse a hacer los exámenes en las fechas definidas para ese periodo escolar
                                (examen
                                de Inglés y de conocimientos en el área a estudiar).</li>
                            <li>En caso de ser aceptado por haber cumplido estrictamente con los puntos anteriores,
                                se entrega
                                la carta de aceptación al aspirante.</li>
                        </ol>

