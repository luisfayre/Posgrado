                </div>
            </div>
            <div class="row bloque">
                <div class="col-md-12 col-sm-12">
                    <p>Para ser Director de Tesis será necesario formar parte del Claustro Doctoral o Consejo de Posgrado. Además, podrá existir un Codirector de Tesis con funciones similares a las del Director de Tesis, quien podrá ser un profesor externo, avalado por el Comité Institucional de Posgrado e Investigación (CIPI).</p>
                    <p>El Director de Tesis desempeñará todas las actividades necesarias para orientar a sus estudiantes, tanto en lo referente a su desarrollo académico y su formación profesional y de investigador, como en lo concerniente a su desarrollo personal, hasta la obtención del grado. Las actividades que se realicen durante la dirección de una tesis se considerarán como dos horas frente a grupo de carga horaria del profesor.</p>
                    <p>Cada estudiante de maestría deberá contar con un director de tesis, quien tendrá la responsabilidad de apoyar en el tema de investigación, para que tenga el nivel de calidad y originalidad requerido. Las funciones del director de tesis son:
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Dirigir al estudiante en su proyecto de tesis.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Coordinar el desarrollo de las actividades académicas del estudiante en la institución.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Turnar, debidamente firmado, el informe de actividades que presentará el estudiante a los integrantes del Comité Tutorial.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Avalar la solicitud escrita del estudiante al Jefe de la DEPI, con el fin de que inicie los trámites para sustentar el examen de grado.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Guardar la confidencialidad debida de los proyectos concertados con los diversos sectores.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Participar como miembro del Jurado.
