                        <h4>PROCESO DE ADMISIÓN</h4>
                        <p>Se integrará un Comité de Admisión con al menos tres profesores del Consejo de Posgrado, quienes analizarán y valorarán la suficiencia académica y la capacidad para realizar investigación del aspirante, mediante los siguientes pasos:</p>
                        <ul>
                            <li>1. Llenar la solicitud de ingreso (FSI-01). En este formato se solicitan datos generales del alumno aspirante, escuela de procedencia y documentación entregada. </li>
                            <li>2. Entrega la Carta de Exposición de Motivos. Es un documento impreso donde se exponen los motivos por los cuales desea ingresar a la maestría (una cuartilla mínimo). Este documento debe ser firmado por el aspirante.</li>
                            <li>3. Presentar dos cartas de recomendación de maestros de licenciatura (excluir maestros del Consejo de Posgrado del Tecnológico de Chihuahua II).</li>
                            <li>4. Pasar con el coordinador de la maestría correspondiente y llevar los documentos señalados en los puntos 1, 2 y 3.</li>
                            <li>5. Presentarse a la entrevista con el Comité de Admisión, según la fecha establecida por el coordinador de la maestría en conjunto con los miembros del Comité de Admisión.</li>
                            <li>6. Presentarse a hacer los exámenes en las fechas definidas para ese periodo escolar (examen de Inglés y de conocimientos en el área a estudiar).</li>
                            <li>7. En caso de ser aceptado por haber cumplido estrictamente con los puntos anteriores, se entrega la carta de aceptación al aspirante.</li>
                        </ul>
