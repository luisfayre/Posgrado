                    <p>El Comité Tutorial estará integrado por profesores investigadores que formen parte del Claustro Doctoral o Consejo de Posgrado y serán designados por éste, adicionalmente podrán participar profesores externos.</p>
                    <p>La División de Estudios de Posgrado e Investigación será el órgano responsable de coordinar a los Comités Tutoriales, con el apoyo del Claustro Doctoral o Consejo de Posgrado. A cada estudiante se le asignará un Comité Tutorial, el cual estará constituido por un mínimo de tres profesores, siendo uno de ellos el Director de Tesis.</p>
                    <p>Serán funciones y responsabilidades del Comité Tutorial, entre las más importantes, las siguientes:
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Planear, coordinar, supervisar y evaluar el desempeño del estudiante durante sus estudios, con apego al plan de trabajo propuesto y aprobado por el Claustro Doctoral o Consejo de Posgrado.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Dictaminar las propuestas de tesis o tesina de los estudiantes tutelados.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Revisar el desarrollo académico y avance del proyecto de tesis o tesina de los estudiantes tutelados.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Evaluar los informes de actividades que presenten los estudiantes.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Participar como Jurado en el seminario pre doctoral, el examen pre doctoral y el examen de grado para el caso de estudios de doctorado.
                        <br>
                        <span class="glyphicon glyphicon-certificate"></span> Revisar la tesis o tesina y, por escrito, informar al sustentante acerca de los cambios y sugerencias recomendados para mejorar el contenido o la presentación de la misma.
                        <br>
                    </p>
